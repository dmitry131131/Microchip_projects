#define F_CPU 1000000UL

#include <avr/io.h>
#include <avr/interrupt.h>
#include <util/delay.h>

// Функция инициализации портов ввода/вывода
void GPIO_Init() {
    // Настройка порта B
    DDRB |= (1 << DDB0);  // Установка PB0 как выход
    PORTB &= ~(1 << PB0); // Установка низкого уровня на PB0
}

// Функция инициализации таймера
void Timer_Init() {
    // Настройка таймера/счетчика0
    TCCR0B |= (1 << CS02) | (1 << CS00); // Предделитель 1024
    TIMSK0 |= (1 << TOIE0);              // Разрешение прерывания по переполнению таймера
}

// Прерывание по переполнению таймера
ISR(TIM0_OVF_vect) {
    // Инвертирование состояния PB0
    PORTB ^= (1 << PB0);
}

int main(void) {
    // Инициализация периферии
    GPIO_Init();
    Timer_Init();

    // Разрешение глобальных прерываний
    sei();

    // Основной цикл программы
    while (1) {
        // Здесь может быть основной код программы
        _delay_ms(100); // Пример задержки
    }

    return 0;
}
